#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char	nombre;

	nombre = '1';
	while (nombre <= '9')
	{
		ft_putchar (nombre);
		nombre += 1;
	}
}
