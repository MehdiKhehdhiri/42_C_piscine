#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int ac, char **ar)
{
	ac += 0;
	ft_putstr(ar[0]);
	ft_putchar('\n');
	return (0);
}
